for (var i = 1; i <= 10; i++) {
  switch(i){
    case 1:
      console.log("*");
      break;
    case 2: 
      console.log("**");
      break;
    case 3:
      console.log("***");
      break;
    case 4:
      console.log("****");
      break;
    case 5: 
      console.log("*****");
      break;
    case 6: 
      console.log("******");
      break;
    case 7:
      console.log("*******");
      break;
    case 8:
      console.log("********");
      break;
    case 9:
      console.log("*********");
      break;
    case 10:
      console.log("**********");
  }

}

